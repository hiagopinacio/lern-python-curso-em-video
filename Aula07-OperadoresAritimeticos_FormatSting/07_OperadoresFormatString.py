x = 'Hiago'

#print da variável com format
print('Olá {}!'.format(x))

#print da variável com format (20 caracteres)
print('Olá {:20}!'.format(x))

#print da variável com format (20 caracteres a direita)
print('Olá {:>20}!'.format(x))

#print da variável com format (20 caracteres a esquerda)
print('Olá {:<20}!'.format(x))

#print da variável com format (20 caracteres centralizados)
print('Olá {:^20}!'.format(x))

#print da variável com format (20 caracteres centralizados entre outro caractere)
print('Olá {:=^20}!'.format(x))

